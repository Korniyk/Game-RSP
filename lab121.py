# Додаток для обчислення суми чисел від 1 до N за допомогою функції sum() та циклу for

# Запитуємо у користувача число
N = int(input("Введіть число N: "))

# Використовуємо функцію sum() та генератор range() для підрахунку суми
summa = sum(i for i in range(1, N + 1))

# Виводимо результат
print(f"Сума чисел від 1 до {N} дорівнює {summa}")
