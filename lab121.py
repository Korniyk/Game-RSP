# Додаток для обчислення суми чисел від 1 до N за допомогою while

# Запитуємо у користувача число
N = int(input("Введіть число N: "))

# Ініціалізуємо змінні
summa = 0
i = 1

# Використовуємо цикл while для підрахунку суми
while i <= N:
    summa += i
    i += 1  # Збільшуємо лічильник

# Виводимо результат
print(f"Сума чисел від 1 до {N} дорівнює {summa}")
