# Додаток для обчислення суми чисел від 1 до N за допомогою for

# Запитуємо у користувача число
N = int(input("Введіть число N: "))

# Ініціалізуємо змінну для збереження суми
summa = 0

# Використовуємо цикл for для підрахунку суми
for i in range(1, N + 1):
    summa += i

# Виводимо результат
print(f"Сума чисел від 1 до {N} дорівнює {summa}")
